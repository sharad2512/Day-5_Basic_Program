import java.util.Scanner;

public class SwapTwoNumber {
    public static void main(String[] args) {
        int a, b, c;
        a = 45;
        b = 3677;
        c = a;

        a = b;
        b = c;
        System.out.println("Swap Value of a : " + a);
        System.out.println("Swap Value of b : " + b);
    }
}
